public class Dasar02_variable {

    public static void main(String[] args){

        // Membuat variabel
        // Tempat menyimpan nilai sesuai tipe-data
        
        // Assigment adalah variable yg memiliki nilai yg sesuai dgn tipe-data
        int a = 10; // Assignment
        System.out.println("nilai a = " + a);

        a = 20;
        System.out.println("nilai a baru = " + a);

        // Membuat sebuah deklarasi
        // Sama seperti membuat variable tapi kosong tanpa nilai
        int b; // Deklarasi / Variable kosong

        b = 7;

        System.out.println("nilai b = " + b);


    }
}